                      median = quantile(value, probs = 0.5, na.rm = TRUE),
                      ub = quantile(value, probs = 0.95, na.rm = TRUE)) %>%
                          bind_cols(xdata)

doses <- sort(unique(xdata$dose))
for(thisDose in doses){
    plotdata <- subset(pred, dose == thisDose)
    p1 <- ggplot(plotdata, aes(x = time, y = cobs))
    p1 <- p1 + geom_point() +
        labs(title = paste(thisDose, "mg\n population predictions"),
             x = "time (h)",
             y = "ME-2 plasma concentration (ng/mL)") +
                 theme(text = element_text(size = 12), axis.text = element_text(size = 12),
                       legend.position = "none", strip.text = element_text(size = 8)) +
                           facet_wrap(~ subject)
    print(p1 + geom_line(aes(x = time, y = median)) +
        geom_ribbon(aes(ymin = lb, ymax = ub), alpha = 0.25))
}

